    private void viewStdInfo() throws SQLException
    {
        if(course.equals("mtech")){
            btechNoDuesPanel.setVisible(false);
        }
        String sqlSelect = "SELECT * FROM `"+course+"` WHERE `Scholar`='"+scholar+"'";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/details","root","");
            Statement stmt=con.createStatement();
            ResultSet rs = stmt.executeQuery(sqlSelect);
            rs.next();
            String Branch=rs.getString("Branch");
            String Exam=rs.getString("Exam");
            String Rank=rs.getString("Rank");
            String Under=rs.getString("Under");
            String Name=rs.getString("Name");
            String Father_Name=rs.getString("Father_Name");
            String Mother_Name=rs.getString("Mother_Name");
            String Address=rs.getString("Address");
            String DOB=rs.getString("DOB");
            String City=rs.getString("City");
            String Image=rs.getString("Image");
            String Contact=rs.getString("Contact");
            String Pin=rs.getString("Pin");
            String Email=rs.getString("Email");
            String State=rs.getString("State");
            String Marriage=rs.getString("Marriage");
            String Blood=rs.getString("Blood");
            String Gender=rs.getString("Gender");
            String Religion=rs.getString("Religion");
            String Belong=rs.getString("Belong");
            String Income=rs.getString("Income");
            String Father_occupation=rs.getString("Father_occupation");
            String Mother_occupation=rs.getString("Mother_occupation");
            String tenSubject=rs.getString("tenSubject");
            String tenBoard=rs.getString("tenBoard");
            String tenPass=rs.getString("tenPass");
            String tenMarks=rs.getString("tenMarks");
            String tweSubject=rs.getString("tweSubject");
            String tweBoard=rs.getString("tweBoard");
            String twePass=rs.getString("twePass");
            String tweMarks=rs.getString("tweMarks");
            String ugSubject=rs.getString("ugSubject");
            String ugBoard=rs.getString("ugBoard");
            String ugPass=rs.getString("ugPass");
            String ugMarks=rs.getString("ugMarks");
            boolean TenScoreCard=rs.getBoolean("TenScorecard");
            boolean TweScoreCard=rs.getBoolean("TweScorecard");
            boolean DOB_Certificate=rs.getBoolean("DOB_Certificate");
            boolean Caste_Certificate=rs.getBoolean("Caste_Certificate");
            boolean DipandBtechScorecard=rs.getBoolean("DipandBtechScorecard");
            boolean PHandEX_Certificate=rs.getBoolean("PHandEX_Certificate");
            boolean Tc=rs.getBoolean("Tc");
            boolean Other=rs.getBoolean("Other");
            File setImage=null;
            if(Image!=null){
                setImage = new File(Image);
                imagePathText.setText(Image);
                jLabel76.setIcon(new ImageIcon(setImage+""));
            }
            
            if(TenScoreCard){
                rd3yes10.setSelected(true);
            }
            else{
                rd3no10.setSelected(true);
            }
            
            if(TweScoreCard){
                rd3yes12.setSelected(true);
            }
            else{
                rd3no12.setSelected(true);
            }
            
            if(DOB_Certificate){
                rd3yesdob.setSelected(true);
            }
            else{
                rd3nodob.setSelected(true);
            }
            
            if(Caste_Certificate){
                rd3yescaste.setSelected(true);
            }
            else{
                rd3nocaste.setSelected(true);
            }
            
            if(DipandBtechScorecard){
                rd3yesmarksheet.setSelected(true);
            }
            else{
                rd3nomarksheet.setSelected(true);
            }
            
            if(PHandEX_Certificate){
                rd3yesph.setSelected(true);
            }
            else{
                rd3noph.setSelected(true);
            }
            if(Tc){
                rd3yesmigration.setSelected(true);
            }
            else{
                rd3nomigration.setSelected(true);
            }
            if(Other){
                rd3yesother.setSelected(true);
            }
            else{
                rd3noother.setSelected(true);
            }
            
            cb3rank.setSelectedItem(Exam);
            cb3branch.setSelectedItem(Branch);
            cb3state.setSelectedItem(State);
            cb3blood.setSelectedItem(Blood);
            cb3religion.setSelectedItem(Religion);
            programmeText.setText(course);
            tx3scholar.setText(scholar);
            tx3rank.setText(Rank);
            tx3name.setText(Name);
            tx3fname.setText(Father_Name);
            tx3mname.setText(Mother_Name);
            tx3add.setText(Address);
            dob3.setText(DOB);
            tx3city.setText(City);
            tx3contact.setText(Contact);
            tx3pincode.setText(Pin);
            tx3email.setText(Email);
            tx3income.setText(Income);
            tx1occupation.setText(Father_occupation);
            jTextField1.setText(Mother_occupation);
            
            //---------------10th---------------
            tx3sub10.setText(tenSubject);
            tx3board10.setText(tenBoard);
            tx3yop10.setText(tenPass);
            tx3marks10.setText(tenMarks);
            //------------12th--------------
            tx3sub12.setText(tweSubject);
            tx3board12.setText(tweBoard);
            tx3yop12.setText(twePass);
            tx3marks12.setText(tweMarks);
            //-----------degree--------------
            tx3subug.setText(ugSubject);
            tx3boardug.setText(ugBoard);
            tx3yopug.setText(ugPass);
            tx3marksug.setText(ugMarks);
            
            
            if(Belong.equals("Ex-Serviceman")){
                chb3ex.setSelected(true);
            }
            else if(Belong.equals("Physically Handicapped")){
                chb3ph.setSelected(true);
            }
            else if(Belong.equals("P.H. and Ex-Serviceman")){
                chb3ex.setSelected(true);
                chb3ph.setSelected(true);
            }
            else{
                chb3ex.setSelected(false);
                chb3ph.setSelected(false);
            }
            
            if(Under.equals("SFS")){
                rd3sfs.setSelected((true));
            }
            else{
                rd3tfws.setSelected(true);
            }
            
            if(Marriage.equals("Merried")){
                rd3married.setSelected((true));
            }
            else{
                rd3unmarried.setSelected(true);
            }
            
            if(Gender.equals("Male")){
                rd3male.setSelected((true));
            }
            else{
                rd3female.setSelected(true);
            }
            
            
            
            
        //------------------------------------------------------------------------------------------------
         //------------------------------------------------------------------------------------------------
         //--------------------------------------Document Details query------------------------------------
         //------------------------------------------------------------------------------------------------
         //------------------------------------------------------------------------------------------------
            String selectDocumentDetails = "SELECT * FROM `"+course+"documentdetails` WHERE Scholar='"+scholar+"'";
            ResultSet rsDocument = stmt.executeQuery(selectDocumentDetails);
            rsDocument.next();
            boolean XMarkValue = rsDocument.getBoolean("XMarkSheet");
            boolean XIIMarkValue = rsDocument.getBoolean("XIIMarkSheet");
            boolean taxDeducationValue = rsDocument.getBoolean("taxDeducation");
            boolean regStdBonValue = rsDocument.getBoolean("regStdBon");
            boolean loanValue = rsDocument.getBoolean("loan");
            boolean aictcAppCertiValue = rsDocument.getBoolean("aictcAppCerti");
            boolean IyearNoDuesValue = rsDocument.getBoolean("IyearNoDues");
            boolean IIyearNoDuesValue = rsDocument.getBoolean("IIyearNoDues");
            String withComplitionCourseDateTextValue = rsDocument.getString("withComplitionCourseDate");
            boolean withComplitionCourseValue = rsDocument.getBoolean("withComplitionCourse");
            String withMidCourseDateTextValue = rsDocument.getString("withMidCourseDate");
            boolean withMidCourseValue = rsDocument.getBoolean("withMidCourse");
            if(XMarkValue){
                XMarkChk.setSelected(true);
            }
            if(XIIMarkValue){
                XIIMarkChk.setSelected(true);
            }
            if(taxDeducationValue){
                taxDeducationChk.setSelected(true);
            }
            if(regStdBonValue){
                regStdBonChk.setSelected(true);
            }
            if(loanValue){
                loanChk.setSelected(true);
            }
            if(aictcAppCertiValue){
                aictcAppCertiChk.setSelected(true);
            }
            if(IyearNoDuesValue){
                IyearNoDuesChk.setSelected(true);
            }
            if(IIyearNoDuesValue){
                IIyearNoDuesChk.setSelected(true);
            }
            if(course.equals("btech")){
                boolean IIIyearNoDuesValue = rsDocument.getBoolean("IIIyearNoDues");
                boolean IVyearNoDuesValue = rsDocument.getBoolean("IVyearNoDues");
                if(IIIyearNoDuesValue){
                    IIIyearNoDuesChk.setSelected(true);
                }
                if(IVyearNoDuesValue){
                    IVyearNoDuesChk.setSelected(true);
                }
            }
            if(withComplitionCourseValue){
                withComplitionCourseChk.setSelected(true);
                withComplitionCourseDateText.setText(withComplitionCourseDateTextValue);
            }
            if(withMidCourseValue){
                withMidCourseChk.setSelected(true);
                withMidCourseDateText.setText(withMidCourseDateTextValue);
            }
            
            
        //-------------------------------------------------------------------------------------------------
        //-------------------------------------------------------------------------------------------------
        //-------------------------------------------------------------------------------------------------
        //-------------------------------------------------------------------------------------------------
        //-------------------------------------------------------------------------------------------------
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tabbedpane.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
